/**
 * This class represents an deportista of the BookStore. 
 * It contains all the information relevant to the author.
 */
export class Deportista {
    /**
    * The deortista id
    */
    id: number;

    /**
     * The deortista name
     */ 
    name: string;

    /**
     * The location of the deportista profile picture
     */
    image: string;

    /**
     * The deportista's birthdate MM/DD/YYYY
     */
    birthDate: string;

    /**
     * A brief description of the deportista's life
     */
    description: string;
}